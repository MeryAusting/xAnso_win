//得到so文件路径
void CxAnso_winDlg::OnBnClickedBtnFileopenpath()
{
	CFileDialog dlg(TRUE, L".so", NULL, OFN_HIDEREADONLY | OFN_OVERWRITEPROMPT, L"可执行文件(*.so)|*.so|所有文件 (*.*)|*.*||");
	if (dlg.DoModal() == IDOK)
	{
		CString m_FilePath = dlg.GetPathName();
		SetDlgItemText(IDC_EDIT1, m_FilePath);
		UpdateData(FALSE);
	}
}

// 重建so后保存路径
void CxAnso_winDlg::OnBnClickedBtnFilesavapath()
{
	WCHAR szDir[MAX_PATH];
	BROWSEINFO bi;
	ITEMIDLIST *pidl;

	bi.hwndOwner = this->m_hWnd;
	bi.pidlRoot = NULL;
	bi.pszDisplayName = szDir;
	bi.lpszTitle = L"请选择目录";
	bi.ulFlags = BIF_RETURNONLYFSDIRS;
	bi.lpfn = NULL;
	bi.lParam = 0;
	bi.iImage = 0;

	pidl = SHBrowseForFolder(&bi);
	if (pidl == NULL){
		return;
	}

	if (SHGetPathFromIDList(pidl, szDir))
	{
		CString m_strSavePath;
		m_strSavePath.Format(L"%s", szDir);
		SetDlgItemText(IDC_EDIT2, m_strSavePath);
	}
}


